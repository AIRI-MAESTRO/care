"""CARE runtime adapters bridging upstream callbacks → Textual messages.

The TUI in :mod:`care.app` is a Textual application; everything async
that drives it (MAGE generation, CARL execution, GigaEvo Platform
evolution) speaks via plain callbacks. The adapters in this package
translate those callback shapes into Textual ``Message`` instances
posted on the running app so screens can react reactively without
coupling to the upstream callback contracts.
"""

